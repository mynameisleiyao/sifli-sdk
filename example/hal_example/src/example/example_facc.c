    uassert_buf_equal(facc_out, iir_iir_output, sizeof(iir_iir_output));    // Validate result

    rt_sem_detach(&facc_sem);
    memset(&facc_sem, 0, sizeof(facc_sem));
    HAL_FACC_DeInit(&hfacc);        // Disable the module when finished
