    NVIC_EnableIRQ(FACC1_IRQn);
    HAL_FACC_Start_IT(&hfacc, fir_fir_input, facc_out, sizeof(fir_fir_input)); // Trigger the FIR calculation, use interrupt mode
    if (rt_sem_take(&facc_sem, INT_WAIT_TIME) != RT_EOK)            // Wait for FACC to finish
    {
        uassert_true(0);                                            // Timeout
    }
    NVIC_DisableIRQ(FACC1_IRQn);
    uassert_buf_equal(facc_out, fir_fir_output, sizeof(fir_fir_output));    // Validate result

    // Demo for IIR
    param = (FACC_ConfigTypeDef *)&iir_iir_param;
